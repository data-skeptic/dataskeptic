<h1>Data Skeptic Internship Position</h1>

<p>Data Skeptic is seeking ambitious students studying fields related to data science that have a passion for science communication to participate in a one month part time internship program.  You'll work directly with Executive Producer Kyle Polich.  During this program, you'll research a topic for a future mini-episode.  You'll be guided to investigate the topic, trained in developing related data visualizations, and instructed on how to prepare a Jupyter notebook that will be used in the show notes.</p>

<p>The internship will last four weeks and your name will be mentioned in the credits of the episode when it goes live.  The work will be conducted remotely with conversations via Slack and Skype.  We have two slots for applicants.  To submit your application, send a resume and cover letter to <a href="mailto:interns@dataskeptic.com">interns@dataskeptic.com</a></p>

<p><center><b>Apply by Friday, May 27th, 2016!</b></center></p>

<h2>Requirements</h2>

<ul>
<li>Actively enrolled in an undergraduate or graduate program related to data science</li>
<li>Prior experience doing a data analysis project in a Project Jupyter notebook.</li>
<li>Hands-on experience working in Python and/or R</li>
<li>Some basic familiarity with Javascript and D3</li>
<li>Work sample on github</li>
<li>Available for at least 4-5 hours of work per week for one month.</li>
<li>Availability after 6pm PST for a weekly check-in</li>
</ul>


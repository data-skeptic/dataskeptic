<ul>
<li>Hands on interaction and mentorship from Kyle Polich</li>
<li>$200 stipend paid upon completion of the internship</li>
<li>Your name mentioned in the credits on Data Skeptic</li>
<li>Opportunity to do a guest blog post on dataskeptic.com linking back to your own web site</li>
</ul>

<h2>How to Apply</h2>

<p>Send your resume to <a href="mailto:interns@dataskeptic.com">interns@dataskeptic.com</a> with a brief statement of why you would like to participate in this program.</p>

